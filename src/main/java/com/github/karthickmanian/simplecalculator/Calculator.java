package com.github.karthickmanian.simplecalculator;

/**
 *  Simple program to understand test
 */
public class Calculator {
    public static Integer add(Integer operator1, Integer operator2) {
        return (operator1 + operator2);
    }

    public static Integer substract(Integer operator1, Integer operator2) {
        return (operator1 - operator2);
    }

    public static Integer multiply(Integer operator1, Integer operator2) {
        return (operator1 * operator2);
    }

    public static Integer divide(Integer operator1, Integer operator2) {
        return (operator1 / operator2);
    }
}
